bool Scene::init()
{
	SkyBox* skyBox = SkyBox::create();
	this->addChild(skyBox);

	Cube* light = Cube::create(CubeType::withLight);
	//light->setTexture("container2.png");
	light->setScale(vec3(0.2f, 0.2f, 0.2f));
	light->setLocalPosition(vec3(0.0f, 15.0f, 10.0f));
	light->setLightSrc(light);
	DEBUG_VEC3(light->transform.position);
	this->addChild(light);

	//Cube* cube = Cube::create(CubeType::withTexAndLight);
	//cube->setTexture("container2.png");
	//cube->setLightSrc(light);
	//cube->setLocalPosition(vec3(0.0f, 0.5f, 0.0f));
	//this->addChild(cube);

	Cube* ground = Cube::create(CubeType::withTexAndLight);
	ground->setScale(vec3(10.0f, 0.2f, 10.0f));
	ground->setLocalPosition(vec3(0.0, -0.5f, 0.0f));
	ground->setTexture("container.jpg");
	ground->setLightSrc(light);
	this->addChild(ground);

	CubeMat* f = CubeMat::create(F);
	f->setLightSrc(light);
	this->addChild(f);

	CubeMat* u = CubeMat::create(U);
	u->setLightSrc(light);
	u->setLocalPosition(vec3(5.0f, u->getLocalPosition().y, 0.0f));
	this->addChild(u);

	CubeMat* c = CubeMat::create(C);
	c->setLightSrc(light);
	c->setLocalPosition(vec3(10.0f, c->getLocalPosition().y, 0.0f));
	this->addChild(c);

	CubeMat* k = CubeMat::create(K);
	k->setLightSrc(light);
	k->setLocalPosition(vec3(15.0f, k->getLocalPosition().y, 0.0f));
	this->addChild(k);

	CubeMat* a = CubeMat::create(A);
	a->setLightSrc(light);
	a->setLocalPosition(vec3(20.0f, a->getLocalPosition().y, 0.0f));
	this->addChild(a);
	return true;
}
